     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validate = $request->validate([
            'image' => ['image','max:1024'],
            'body' => ['required']
        ]);

        if($request->file('image'))
        {
            if($request->oldimage)
            {
                Storage::delete($request->oldimage);
            }
            $validate['image'] = $request->file('image')->store('intro');
        }

        intro::where('id', $id)->update($validate);

        return redirect('/dashboard/intro')->with('success', 'Data has been updated');
    }

